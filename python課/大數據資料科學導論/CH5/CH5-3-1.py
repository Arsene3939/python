# 紅色LED 0x20
# 綠色LED 0x21
# 藍色LED 0x22

import smbus
from time import sleep

# 建構I2C物件
myI2C = smbus.SMBus(1) # 指定使用/dev/i2c-1
# I2C位址
R = 0x20
G = 0x21
B = 0x22
_code=[ 0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x40,0x20,0x10,0x08,0x04,0x02,
0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x40,0x20,0x10,0x08,0x04,0x02,0x01,0x18,0x3C,
0x7E,0xFF,0x7E,0x3C,0x18,0x18,0x3C,0x7E,0xFF,0x7E,0x3C,0x18,0x18,0x3C,0x7E,0xFF,0x7E,
0x3C,0x18,0x18,0x3C,0x7E,0xFF,0x7E,0x3C,0x18]
#################################
while True:
    for i in range(len(_code)) : 
        myI2C.write_byte(R, 0xFF^_code[i])
        myI2C.write_byte(G, 0xFF^_code[i-1])
        myI2C.write_byte(B, 0xFF^_code[i-2])
        sleep(0.08)
        